# Ejercicio 8: Explicación de conceptos con ejemplos humanos
# 1. Unidad central de procesamiento (CPU): Es como el cerebro humano, que procesa la información y toma decisiones.
# 2. Memoria principal: Es como la memoria a corto plazo, donde almacenamos temporalmente la información que estamos utilizando.
# 3. Memoria secundaria: Es como la memoria a largo plazo, donde guardamos información importante que no necesitamos de inmediato.
# 4. Dispositivos de entrada: Son como nuestros sentidos (ojos, oídos), que nos permiten recibir información del mundo exterior.
# 5. Dispositivos de salida: Son como nuestras respuestas o expresiones (hablar, escribir), que nos permiten enviar información al exterior.

